import turtle

window = turtle.Screen()
window.screensize(500,500)
window.bgcolor("white")
t = turtle.Turtle()
t.color("red")
t.speed(1)
t.begin_fill()
t.fd(100)
t.lt(45)
t.fd(100)
t.lt(45)
t.fd(100)
t.lt(45)
t.fd(100)
t.lt(45)
t.fd(100)
t.lt(45)
t.fd(100)
t.lt(45)
t.fd(100)
t.lt(45)
t.fd(100)
t.lt(45)
t.end_fill()

t.penup()
t.fd(50)
t.rt(90)
t.color("black")
t.begin_fill()
t.pendown()
#make a rectangle with 300 in height and 10 in width
t.fd(300)
t.lt(90)
t.fd(10)
t.lt(90)
t.fd(300)
t.lt(90)
t.fd(10)
t.end_fill()

t.up()
t.color("white")
t.rt(180)
t.goto(0,0)
t.bk(45)
t.lt(90)
t.fd(100)
t.write("STOP", font=("Arial", 50, "bold"))
turtle.exitonclick()