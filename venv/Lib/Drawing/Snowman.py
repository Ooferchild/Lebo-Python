import turtle

#snowman
window = turtle.Screen()
window.title("Snowman by Josh")
window.screensize(100,200)
window.bgcolor("cyan")
t = turtle.Turtle()
t.color("white")
t.speed(7)

#circles
t.goto(0,-90)
t.begin_fill()
t.circle(50)
t.end_fill()
t.up()
t.goto(0,-20)
t.down()
t.begin_fill()
t.circle(40)
t.end_fill()
t.up()
t.goto(0,35)
t.down()
t.begin_fill()
t.circle(30)
t.end_fill()

#hat
t.up()
t.goto(-25,90)
t.down()
t.color("black")
t.begin_fill()
t.fd(50)
t.lt(90)
t.fd(10)
t.lt(90)
t.fd(50)
t.lt(90)
t.fd(10)
t.end_fill()
t.up()
t.goto(10,90)
t.lt(180)
t.down()
t.begin_fill()
t.fd(40)
t.lt(90)
t.fd(20)
t.lt(90)
t.fd(40)
t.lt(90)
t.fd(20)
t.end_fill()

#eyes
t.up()
