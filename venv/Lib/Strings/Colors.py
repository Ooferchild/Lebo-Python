color = input('What is your favorite color? ')
colorFixed = color.lower()
if colorFixed == 'red':
    print('Red is the color of wine')
elif colorFixed == 'orange':
    print('Orange is the color of sunsets')
elif colorFixed == 'yellow':
    print('Yellow is the color of bananas')
elif colorFixed == 'green':
    print('Green is the color of the earth')
elif colorFixed == 'blue':
    print('Blue is the color of the sky')
elif colorFixed == 'purple':
    print('Purple is the color of grapes')
else:
    print('Pick a better color')